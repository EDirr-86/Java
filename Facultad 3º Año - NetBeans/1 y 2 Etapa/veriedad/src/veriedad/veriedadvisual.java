    private void botonveriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonveriActionPerformed
    Calendar cal=Calendar.getInstance(); 
    int dd=cal.get(cal.DATE);
    int mm=cal.get(cal.MONTH)+1;     
    int aa=cal.get(cal.YEAR);
    int dia,mes,anio;
    
    dia = Integer.parseInt(caja1.getText());
    mes = Integer.parseInt(caja2.getText());
    anio = Integer.parseInt(caja3.getText());
    
    boolean veri= false;
    
    if (aa - anio > 18)
    {veri = true;
        if (mm == mes)
                {if (dd == dia)
                    {veri=true; etfecha.setText("FELIZ CUMPLE");etfecha.setForeground(Color.ORANGE);
                    }}}
    else
        if (aa - anio == 18)
            if (mm > mes)
            {if (dd > dia)
                    veri = true;}
            else
                if (mm == mes)
                {if (dd == dia)
                    {veri=true; etfecha.setText("FELIZ CUMPLE");etfecha.setForeground(Color.ORANGE);
                    }}
    if (aa - anio < 18)
        if (mm == mes)
            if(dd == dia)
               {etfecha.setText("FELIZ CUMPLE");etfecha.setForeground(Color.ORANGE);
                    }
                         
      
        if (veri) {
            etveri.setText("MAYOR DE EDAD");
            etveri.setForeground(Color.GREEN);
        }
        else {
            etveri.setText("MENOR DE EDAD");
            etveri.setForeground(Color.RED);
        }
    
    }//GEN-LAST:event_botonveriActionPerformed

