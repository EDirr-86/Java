    private void beliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliminarActionPerformed
int fila;
double resta=0;
fila=tabla.getSelectedRow();
if(fila>=0)
{
    objtabla.removeRow(fila);
}

for(int j=0;j<tabla.getRowCount();j++)  //getowcountcantidad filas
{
resta=resta+Double.parseDouble((String)tabla.getValueAt(j, 3));//getvalueat  toma el valor de
}
txtsuma.setText(""+resta);

    }//GEN-LAST:event_beliminarActionPerformed

    private void bagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bagregarActionPerformed
  String datos[]=new String [4];

double suma=0;
datos[0]=(String) combo1.getSelectedItem(); //paso los datos del combo a la tabla
datos[1]=txtvalor.getText();
datos[2]=caja2.getText();
datos[3]= String.valueOf((Double.parseDouble(txtvalor.getText())) * (Double.parseDouble(caja2.getText())));;

objtabla.addRow(datos);

txtsuma.setText("");
for(int i=0;i<tabla.getRowCount();i++)
{
suma=suma+Double.parseDouble((String)tabla.getValueAt(i, 3));
}
txtsuma.setText(""+suma);
txtvalor.setText("");
caja2.setText("");
combo1.setSelectedIndex(-1);
    }//GEN-LAST:event_bagregarActionPerformed

