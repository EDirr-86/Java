    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       try 
           {
            Conectar miconexion = new Conectar ();
            
            String cons = "select * from remito where idremito = " + txtnroremito.getText();
            ResultSet consulta =miconexion.consulta(cons);
            if (consulta.last()==true)
                {
                    txtfecharemito.setText(consulta.getString(2));
                    txtcliente.setText(consulta.getString(3));
                    txtdomicilio.setText(consulta.getString(4));
                    txtlocalidad.setText(consulta.getString(5));
                    txtcuit.setText(consulta.getString(6));
                    
  
                    
                    cons = "select * from detalleremito where idremito = " + txtnroremito.getText();
                    consulta =miconexion.consulta(cons);
                    String datos[]=new String [4];
                    
                    do 
                    {
                    consulta.next();      
                    datos[0]=String.valueOf(consulta.getString(2));
                    datos[1]=String.valueOf(consulta.getString(3));
                    datos[2]=String.valueOf(consulta.getString(4));
                    Double resultado = Double.parseDouble(consulta.getString(3)) * Double.parseDouble(consulta.getString(4));
                    datos[3] = String.valueOf(resultado);
                    objtabla.addRow(datos);  
                    }
                    while (consulta.isLast()==false);      
                }

           }
        catch (Exception e)
             {
              JOptionPane.showMessageDialog(null, e.getCause());
             }
       
    }//GEN-LAST:event_jButton3ActionPerformed

