    private void botonguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonguardarActionPerformed
        
        
 
        try 
        {
         Conectar miconexion = new Conectar ();
         String sentenciaInsert = "insert into remito values (?,?,?,?,?,?,?) ";
         
         miconexion.psPrepararSentencias = miconexion.miconexion.prepareStatement(sentenciaInsert);
         miconexion.psPrepararSentencias.setString(1,txtnroremito.getText());
         miconexion.psPrepararSentencias.setString(2,(txtfecharemito.getText()));
         miconexion.psPrepararSentencias.setString(3,txtcliente.getText());
         miconexion.psPrepararSentencias.setString(4,txtdomicilio.getText());
         miconexion.psPrepararSentencias.setString(5,txtlocalidad.getText());
         miconexion.psPrepararSentencias.setString(6,txtcuit.getText());
         
         if (botonconsfinal.isEnabled())
                miconexion.psPrepararSentencias.setString(7,"1");
         if (botonexcento.isEnabled())
                miconexion.psPrepararSentencias.setString(7,"2");
         
         miconexion.psPrepararSentencias.executeUpdate();
         
         
         for (int i=0; i<tabla.getRowCount();i++)
            {
             sentenciaInsert = "insert into detalleremito values (?,?,?,?) ";
             miconexion.psPrepararSentencias = miconexion.miconexion.prepareStatement(sentenciaInsert);
             miconexion.psPrepararSentencias.setString(1,txtnroremito.getText());
             miconexion.psPrepararSentencias.setString(2,tabla.getValueAt(i, 0).toString());
             miconexion.psPrepararSentencias.setString(3,tabla.getValueAt(i, 1).toString());
             miconexion.psPrepararSentencias.setString(4,tabla.getValueAt(i, 2).toString());
             miconexion.psPrepararSentencias.executeUpdate();
            }
         

         
         
        }
        catch (Exception e)
             {
              JOptionPane.showMessageDialog(null, e.getCause());
             }
        
        
                        
        
    }//GEN-LAST:event_botonguardarActionPerformed

